function H = get_projective(pt1, pt2)
    A=[pt1(1,1) pt1(1,2) 1 0 0 0 -pt2(1,1)*pt1(1,1) -pt2(1,1)*pt1(1,2);             
       0 0 0 pt1(1,1) pt1(1,2) 1 -pt2(1,2)*pt1(1,1) -pt2(1,2)*pt1(1,2);
       pt1(2,1) pt1(2,2) 1 0 0 0 -pt2(2,1)*pt1(2,1) -pt2(2,1)*pt1(2,2);             
       0 0 0 pt1(2,1) pt1(2,2) 1 -pt2(2,2)*pt1(2,1) -pt2(2,2)*pt1(2,2);
       pt1(3,1) pt1(3,2) 1 0 0 0 -pt2(3,1)*pt1(3,1) -pt2(3,1)*pt1(3,2);             
       0 0 0 pt1(3,1) pt1(3,2) 1 -pt2(3,2)*pt1(3,1) -pt2(3,2)*pt1(3,2);
       pt1(4,1) pt1(4,2) 1 0 0 0 -pt2(4,1)*pt1(4,1) -pt2(4,1)*pt1(4,2);             
       0 0 0 pt1(4,1) pt1(4,2) 1 -pt2(4,2)*pt1(4,1) -pt2(4,2)*pt1(4,2);];
    
    B = [pt2(1,1);pt2(1,2);pt2(2,1);pt2(2,2);pt2(3,1);pt2(3,2);pt2(4,1);pt2(4,2)];
    
    out = A\B;
    
    H = [out(1) out(2) out(3);
         out(4) out(5) out(6);
         out(7) out(8) 1];
end

